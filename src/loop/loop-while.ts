// while loop

console.log("====================================");
let i = 0;
while (i < 5) {
    console.log(i);
    i++;
}

console.log("====================================");

let arr = [1, 2, 3, 4, 5];
let j = 0;
while (j < arr.length) {
    console.log(arr[j]);
    j++;
}

console.log("====================================");

let str = 'hello';
let k = 0;
while (k < str.length) {
    console.log(str[k]);
    k++;
}

